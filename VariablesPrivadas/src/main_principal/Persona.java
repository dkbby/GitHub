package main_principal;

public class Persona {

	private String nombre;
	private String apellidos;
	private int edad;
	

	public void poner_nombre(String nombre)
	
	{
		
	     this.nombre = nombre;
	    
	     
	}
	
	
	public String devolver_nombre()
	
	{
	
	     return nombre;
	     
	}

	public void poner_apellidos(String apellidos)
	
	{
		
	     this.apellidos = apellidos;
	    
	     
	}
	
	
	public String devolver_()
	
	{
	
	     return apellidos;
	     
	}

	public void poner_edad(int edad)
	
	{
		
	     this.edad = edad;
	    
	     
	}
	
	
	public int devolver_edad()
	
	{
	
	     return edad;
	     
	}
}
